/**
 * Vue.js 支持所有兼容 ECMAScript 5 的浏览器。
 * 1.直接下载并用 <script> 标签引入，Vue 会被注册为一个全局变量。
 * 在开发时请用开发版本，遇到常见错误它会给出友好的警告。
 * 2.CDN
 * 推荐：unpkg(https://unpkg.com/vue/dist/vue.js),
 * 会保持和 npm 发布的最新的版本一致。可以在 unpkg.com/vue/ 浏览 npm 包资源。
 * 3.NPM
 * 在用 Vue.js 构建大型应用时推荐使用 NPM 安装(npm install vue)，
 *  NPM 能很好地和诸如 Webpack 或 Browserify 模块打包器配合使用。
 * Vue.js 也提供配套工具来开发单文件组件。
 * vue.common.js  独立构建 包含模板编译和支持 template 选项
 * vue.js - 运行时构建 - 不包含模板编译也不支持 template 选项。
 * 4.命令行工具
 * Vue.js 提供一个官方命令行工具，可用于快速搭建大型单页应用。
 * 该工具提供开箱即用的构建工具配置，带来现代化的前端开发流程。
 * 只需几分钟即可创建并启动一个带热重载、保存时静态检查以及可用于生产环境的构建配置的项目：
 *   # 全局安装 vue-cli
     $ npm install --global vue-cli
     # 创建一个基于 webpack 模板的新项目
     $ vue init webpack my-project
     # 安装依赖，走你
     $ cd my-project
     $ npm install
     $ npm run dev
 * 5.AMD 模块加载器
 * 独立下载版本或通过 Bower 安装的版本已用 UMD 包装，因此它们可以直接用作 AMD 模块。
 */




















