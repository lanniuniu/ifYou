/*
内置常用模块
模块                 引入                         使用目的
BrowserModule       @angular/platform-browser    应用在浏览器能运行
CommonModule        @angular/common              使用 NgIf 和 NgFor等
FormsModule         @angular/forms               构建模板驱动表单时（它包含NgModel）
ReactiveFormsModule @angular/forms               构建响应式表单
RouterModule        @angular/router              要使用路由功能，并且你要用到 RouterLink,.forRoot() 和 .forChild() 时
HttpClientModule    @angular/common/http         发起http请求
 */

